2. Reglas de Juego

2.1. Serán de aplicación las Reglas de Juego FIFA 2010/2011.

2.2. Definición de los Torneos Si al término de los partidos de desempate el resultado se mantiene igualado, las definiciones se operarán de la siguiente manera: Se jugarán diez (10) minutos adicionales, divididos en dos períodos de cinco (5) minutos cada uno, con tiempo cronometrado y sin gol de oro. De persistir la igualdad, se ejecutará una serie de cinco (5) penales alternativos por equipo, y de mantenerse la igualdad, se ejecutarán series de un (1) penal por equipo, hasta que se produzca el desnivel que consagre un ganador (Reglas de Juego FIFA 2010/2011).

2.2.1. Durante la disputa del tiempo suplementario se deberán contemplar los siguientes detalles:

a. las faltas acumuladas en el segundo período del tiempo regular no se borran en caso de tener que disputar tiempo suplementario, quedando automáticamente vigentes para la disputa de los dos períodos de la misma

b. los entrenadores no podrán solicitar el minuto de tiempo muerto en ninguno de los períodos del tiempo suplementario

c. tras la conclusión del primer período del tiempo suplementario, no habrá descanso, debiendo los equipos cambiar de lado inmediatamente.

2.2.2 En los casos de definición del Torneo de 4º División, si al término de los partidos de desempate el resultado se mantiene igualado, la definición se operará de la siguiente manera: se ejecutará una serie de cinco (5) penales alternativos por equipo, y de mantenerse la igualdad, se ejecutarán series de un (1) penal por equipo, hasta que se produzca el desnivel que consagre un ganador (Reglas de Juego FIFA 2010/2011).

3. Presentación de Divisiones

4ta. División: jugadoras nacidas en el año 2006, 2005, 2004, 2003 y 2002.

3ra. División: jugadoras nacidas en el año 2001, 2000, 1999, 1998 y 1997.

1ra. División: jugadoras nacidas con anterioridad al 01-01-1996.

3.1. No podrán participar en los partidos de 1ra. División aquellas jugadoras que no hayan cumplido quince (15) años de edad.

3.2. No podrán participar en ninguna División, las jugadoras comprendidas en las categorías 2007 o posteriores.

3.3. En 3ra. División los clubes podrán inscribir en las planillas de partidos hasta dos (2) jugadoras comprendidos en las categorías 1996 y/o anteriores. Dichas jugadoras podrán participar en los partidos de 1ra. División contra el mismo rival correspondiente a esa fecha.

Las jugadoras comprendidas en las categorías 2001, 2000, 1999, 1998 y 1997 que hayan sido inscriptas en la Lista de Buena Fe correspondiente a la 1ra. División, para su actuación en 3ra. División serán consideradas como ocupantes de las plazas previstas para las edades de excepción del párrafo anterior.

3.3.1. Las jugadores inscriptas en 3ra. y 1ra. División no podrán participar en ningún partido de 4ta. División. Las jugadoras inscriptas en 4ta. podrán jugar en 3ra. pero no podrán participar en los partidos de 4ta. División contra el mismo rival correspondiente a esa fecha.

3.4. Aquel club que incluya en su alineación una jugadora no clasificada, o inhabilitada por el Departamento de Registro de Jugadores o el Departamento de Medicina Deportiva, o en infracción a algún impedimento previsto en este Reglamento, será sancionado con la pérdida de puntos de encuentros.

3.5. Será obligatorio el uso en todos los equipos de todas las categorías, de la indumentaria oficial, numerando las casacas desde el Nº 1 al Nº 25. Todas las divisiones deberán estampar los números en la parte de atrás. Las jugadoras de todas las categorías deberán vestir correctamente el uniforme de su club, que consistirá en: camisa o camiseta, pantalón corto, medias y zapatillas con suela “lisa”. Podrán usar también guantes, tobilleras, musleras, rodilleras y/o muñequeras elásticas, con la obligatoriedad del uso de canilleras. En el caso de los arqueros podrán utilizar pantalón largo. Las capitanas de los equipos deberán distinguirse con un brazalete que contraste con el color de la camiseta, colocado en el brazo izquierdo. Los árbitros no comenzarán los encuentros si alguno de los equipos incurriere en falta respecto del uniforme oficial, con la consiguiente pérdida de puntos del partido.

4. Fechas y horarios

Reglamento Campeonato de Futsal Femenino 2016

Los partidos de los Campeonatos de Futsal se disputarán desde los días sábados y/o domingos desde las 10 hr AM hasta las 22 hr PM, siendo este el horario de finalización del último encuentro de esa fecha.

Las 4ta divisiones no podrán jugar NUNCA después de las 20 hr PM.

5. Partidos Suspendidos

Cuando por inclemencias del tiempo o situaciones extremas fuera suspendido un partido de los Campeonatos de Futsal Femenino, los mismos deberán disputarse:

a. Dentro de los quince (15) días posteriores a su suspensión o fallo de T.D.D. que disponga su reprogramación o continuación, siempre que no coincida con la disputa de las tres últimas fechas del respectivo certamen.

b. Dentro de los cinco (5) días posteriores a su suspensión o fallo de T.D.D. que disponga su reprogramación o continuación, cuando coincidiera con las tres últimas fechas del respectivo certamen.

c. Dentro de los cinco (5) días posteriores a su suspensión o fallo de T.D.D. que disponga su reprogramación o continuación, cuando el respectivo certamen ya hubiera finalizado.

6. Campos de Juego

El campo de juego deberá estar correctamente demarcado, conforme a las especificaciones técnicas exigidas y contenidas en las Reglas de Juego FIFA 2008 y editadas por la Comisión de Futsal a la fecha de su verificación por miembros de la Comisión de Estadios para proceder a su habilitación.

7. Tiempos de Juegos

La duración de los partidos será la siguiente: 1ª división: El encuentro se jugará en dos tiempos iguales de veinticinco (25) minutos cada uno, basado en las Reglas de Juego FIFA 2004 Nº 7 y Nº 8.

3ª división: El encuentro se jugará en dos tiempos iguales de veinte (20) minutos cada uno, basado en las Reglas de Juego FIFA 2004 Nº 7 y Nº 8.

4ª división: El encuentro se jugará en dos tiempos iguales de quince (15) minutos corridos cada uno, cronometrado por el mismo árbitro con reloj de mano.

Para la 1° y 3° división, el tiempo de descanso durante el intervalo entre el primer y segundo período será de diez (10) minutos, para la 4° división, el tiempo de descanso será de cinco (5) minutos.

8. Registro y Costos de Inscripción de Jugadoras

Reglamento Campeonato de Futsal Femenino 2016

Los clubes o equipos deberán inscribir un mínimo de cinco (8) jugadoras en Cuarta División, de diez (10) en Tercera División y de doce (12) en Primera División; con un máximo de veinte (20) jugadoras para cada una de las categorías. La no inscripción mínima de jugadoras por categoría hará que el club o equipo quede automáticamente eliminado del Torneo.

8.1. Los clubes o equipos deberán presentar las fichas médicas de las jugadoras clasificadas y remitidas como “nuevas” para su intervención en el torneo, los clubes deberán presentar conjuntamente con el listado de cada una de ellas, el apto médico de cada jugadora certificado con la firma y sello del Presidente, Secretario y Médico que la asistió en el control.

8.2. Los Registros de Inscripción de Jugadoras para intervenir en estos certámenes permanecerán abiertos hasta el comienzo del torneo.

8.3. Se establece la suma de quinientos ($500), por la inscripción de cada categoría (4ta- 3era y 1era), la que será abonada en S.O.F.Fem, en un solo pago, adjuntando el recibo de comprobante correspondiente a las Listas de Buena Fe que presente el club al momento de efectuar el trámite de inscripción en el Departamento de Registro de Jugadores. Los clubes que disputen todos los partidos correspondientes a la 4ta. División, obtendrán como premio, un descuento del 50% en el importe que se establezca para el Torneo 2017 en concepto de suma anual para la inscripción de las jugadoras.

8.4. Mensualmente cada jugadora abonara de manera conjunta por categoría de su equipo y Club la suma de cien ($100) en concepto de Emergencias Medicas y gastos administrativos que S.O.F.Fem garantiza en cada fecha disputada. De no efectuarse dicho pago la jugadora queda fuera de competencia hasta la normalización del mismo, siendo este no acumulable.

8.5. Cada equipo o Club se inscribirá de manera on line por medio de una contraseña personal por equipo o Club la cual S.O.F.Fem designara al certificar interés de participación de los mismos.

8.6 Cada equipo deberá imprimir las listas de fichaje y tenerlas listas en el Club al día designado por S.O.F.Fem, donde las autoridades pasaran a retirarlas, y en ese momento serán firmadas por las jugadoras con DNI presente.

8.7 De no efectuarse ese paso (9.6), la jugadora no podrá ser parte del torneo.

8.8 Las jugadoras de 4ta división deberán estar presentes con alguno de sus padres y/o tutor, quien adjuntara su firma a la de la jugadora en concepto de aval a la participación. El punto 9.6 se adhiere a este.

8.9 Las planillas se imprimen y firman por triplicado, quedando una planilla al Club o equipo, una a S.O.F.Fem y una copia de seguridad.

9. Liquidación de Partidos

9.1 Cada jugadora abonara por partido la suma de veinte ($20) que la habilitara a participar, la cual el delegado, Dt o responsable del equipo Club o categoría deberá entregar 15 minutos antes de iniciarse el encuentro.

9.2 El faltante de pago dejara a la jugadora o equipo fuera de competencia.

10. Balones

10.1 Los balones serán oficiales de S.O.F.Fem poniéndose en juego para las jornadas a disputarse.

10.2 La compra de los mismos se hará mediante pedido a S.O.F.Fem el cual expenderá al Club o equipo.

11. Obligaciones generales

11.1 Cada jornada contara con un servicio de emergencia la cual S.O.F.Fem garantiza para la atención de cualquier incidente que de ella requiera.

11.2 Cada jornada contara con un policía uniformado o un móvil de seguridad que asegure la integridad y armonía de los encuentros.

11.3 El faltante de cualquiera de ellos, retrasara la jornada hasta pudiéndose suspender por dicho motivo.

11.4 El buffet de las sedes designadas no podrán vender bebidas alcohólicas bajo ninguna excepción.

11.5 Las sedes designadas cobraran un ingreso a los partidos, para los acompañantes de las jugadoras de veinte ($20) o un alimento no perecedero que se designara en conjunto con S.O.F.Fem su destino, siendo estos comedores u hogares del distrito de Hurlingham.

11.6 El comportamiento inadecuado de cualquier persona que esté dentro del establecimiento presenciando la jornada, esta la involucre o no, puede ser invitado a retirarse, con la devolución de su entrada en valor o alimento entregado.

11.6 Las jugadoras no abonan entrada.

12. Planillas de partidos

12.1 Cada Club o equipo debe presentarse con la planilla de jugadoras impresa desde la pagina www.soffem.com donde estarán las jugadoras habilitadas, las cuales firmaran con DNI en mano frente la mesa de control, previo al partido.

12.2 De no estar en la lista, no podrá participar del encuentro.

