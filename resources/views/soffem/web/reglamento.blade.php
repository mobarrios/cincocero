2. Forma de disputa

Mediante el desarrollo del Torneo “Clausura” 2016, denominado Torneo Presentación.

2.1. Torneo Clausura “Presentación 2016” Se desarrollará mediante un (1) Torneo, de (1) Fase, iniciándose el mismo con puntaje "cero" para cada club o equipo participante.

2.2. Primera Fase: Se desarrollará por el sistema de todos contra todos, en una rueda de 1 partido jugado en las diferentes sedes designadas por S.O.F.Fem, por puntos (3 puntos al equipo ganador – 1 punto para cada equipo en empates y 0 para el equipo derrotado) conforme al programa de partidos sorteado oportunamente. En caso de empate en puntos entre dos o más equipos, para definir alguna ubicación en la Tabla Final de Posiciones del Torneo “Clausura 2016, serán de aplicación las disposiciones del Reglamento General.

2.3. Final: El equipo con mayor cantidad de puntos será el campeón del Torneo, asi en cada categoría (4ta-3era y 1era).

En caso de que dos equipos hayan llegado con misma cantidad de puntos al final del torneo, se enfrentarán a un solo partido, en cancha neutral a definirse oportunamente, sin tenerse en cuenta la cantidad de goles con la que haya llegado a esa instancia. Si al término de este partido el resultado se mantiene igualado la definición se operará mediante la disputa de un tiempo suplementario de diez (10) minutos, en dos períodos de (5) minutos cada uno (basado en las y, en caso de subsistir la igualdad, la definición se resolverá mediante la ejecución de tiros desde el punto penal. (Reglas de Juego FIFA 2006 - Tiros desde el Punto Penal).

2.4. El ganador de esta final será proclamado campeón del Torneo “Clausura Presentación 2016”.

